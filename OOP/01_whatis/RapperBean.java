 public class RapperBean implements java.io.Serializable{
	private int songs;
	private int age;
	private String name;
	
	public int getSongs() {
		return songs;
	}
	public void setSongs(int psongs) {
		songs = psongs;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int pAge) {
		age = pAge;
	}
	public String getName() {
		return name;
	}
	public void setRealName(String pRealName) {
		name = pRealName;
	}   
}
